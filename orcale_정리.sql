 				LPAD(데이터, 데이터 자릿수, 채울 문자) : 왼쪽에 채울건지
 				RPAD() : 오른쪽에 채울건지
 					
 		2. 숫자 함수
 			1) ROUND(지정 문자열, 몇번째에서 할지) : 반올림
 			2) TRUNC(지정 문자열, 몇번째에서 할지) : 버림
 			3) CEIL() : 가장 큰 정수
 			4) FLOOR() : 가장 작은 정수
