					TO_CHAR(SYSDATE, 'HH:MI:SS PM') => 05:59:04 오후
		
					* 12시간제 이므로 HH24 라고 명시하지 않으면 05:59:04 식으로 도출
					* AM, PM => 아무거나 주면 오전/오후 나옴
				
				9 : 숫자 한자리를 의미
				0 : 숫자 한자리를 의미(빈자리를 0으로 채움)
 			2) TO_NUMBER('문자열데이터','인식할숫자형태') : 문자열 데이터를 숫자 데이터로 반환
