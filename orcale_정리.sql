 			3) TO_DATE(날짜 형식에 맞는 숫자, 날짜와 맞춘 형식) : 문자열 데이터를 날짜 데이터로 반환
 	
 				* NUMBER + '문자숫자' => 덧셈 가능 => 자동형변환
 			
 		5. NULL
